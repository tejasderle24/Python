# C. Logical Operators: 

# 1. and => Both are True then True otherwise False
print(True and True)
print(True and False)
print(False and True)
print(False and False)

# 2. or =>  Both are False then False otherwise True
print("OR Operaters")
print(True or True)
print(True or False)
print(False or True)
print(False or False)

# 3. not 
print("NOT Operaters")
print(not(True))
print(not(False))