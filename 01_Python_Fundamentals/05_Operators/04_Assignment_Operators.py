# D. Assignment Operators

# = 
a = 32
print(a)

# += 
a += 2   # Equivalent to x = x + 2
print(a)

# -= 
a -= 3   # Equivalent to x = x - 3
print(a)

#  *= 
a *= 2   # Equivalent to x = x * 2
print(a)

# /= 
a /= 2   # Equivalent to x = x / 2
print(a)

# %= 
a %= 2   # Equivalent to x = x % 2
print(a)

# **= 
a **= 2      # Equivalent to x = x ** 2
print(a)

# //=
a //= 3      # Equivalent to x = x // 3
print(a)